function  sum(a:number, b:number):number {
    return a + b;
}
export {
    sum
}

function  combine(a:string, b:string):string {
    return a + b;
}
export {
    combine
}

function  arr(a:number, b:number):number[] {
    return [a,b];
}
export {
    arr
}

function  obj(a:number, b:number):object {
    return {a,b};
}
export {
    obj
}